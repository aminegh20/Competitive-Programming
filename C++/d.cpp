constexpr int MAX = (int) 2e5 + 1;

int main () {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n, k;
  cin >> n >> k;
  vector<int> a(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i];
  }
  vector<multiset<int, greater<int>>> mp(MAX);
  vector<int> sum(MAX);
  for (int i = 0; i < n; i++) {
    int x = a[i];
    int moves = 0;
    int has = -1;
    while (true) {
      if (mp[x].size() < k) {
        mp[x].emplace(moves);
        sum[x] += moves;
      } else if (*mp[x].begin() > moves) {
        sum[x] -= *mp[x].begin();
        mp[x].erase(mp[x].begin());
        mp[x].emplace(moves);
        sum[x] += moves;
      }
      moves += 1;
      x >>= 1;
      if (x == 0) has++;
      if (has == 1) break;
    }
  }
  int ans = (int) 1e9;
  for (int i = 0; i < MAX; i++) {
    if (mp[i].size() == k) {
      ans = min(ans, sum[i]);
    }
  }
  cout << ans << '\n';
  return 0;
}